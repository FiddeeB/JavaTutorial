
public class Application {
	public static void main(String[] args) {
		int value = 0; // sätter value till 0
		
		
		
		while(value < 15) //sätter en loop med kraven att value ska vara mindre än 15
		{
			System.out.println("Hello " +  value); //skriver ut text + value
			
			value = value + 1; //loopens ökning. i detta fallet ökar value med 1 för varje gång
		}
	}

}
